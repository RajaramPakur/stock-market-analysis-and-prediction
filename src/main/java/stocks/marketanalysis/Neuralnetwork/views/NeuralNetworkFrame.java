    private void btnTrainActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTrainActionPerformed

        System.out.println("------------ Training session ---------------");
        for(int i=0; i<objNS.getEpochCount(); i++){
           System.out.println(" Iteration : " + i);
           double[] target = {data[i%8][3]};
           objNN.BackPropagation(data[i%8], target);
           
           System.out.println(" Mean SE : "+objNN.MeanSquareError(target));
           if(objNN.MeanSquareError(target) < objNS.getThreshold()){
               break;
           }
        }
    }//GEN-LAST:event_btnTrainActionPerformed

