    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed

        if (txtLearningRate.getText().equals("") || txtLayer.getText().equals("") || txtLayerSizes.getText().equals("") || txtThreshold.getText().equals("") || txtIteration.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Empty field");
        } else {
            String[] split = txtLayerSizes.getText().split(",");

            if (Integer.parseInt(txtLayer.getText()) == split.length) {
                int[] lSize = new int[split.length];
                int i = 0;
                for (String string : split) {
                    lSize[i] = Integer.parseInt(string);
                    i++;
                }

//          All the Configuration parameters are initialize and assign using NetworkSettings class
                objNS = new NetworkSettings(Double.parseDouble(txtMomentum.getText()), Double.parseDouble(txtLearningRate.getText()), Integer.parseInt(txtIteration.getText()), Integer.parseInt(txtLayer.getText()), lSize, Double.parseDouble(txtThreshold.getText()));

//          The Neural network is design as the config parameters
                objNN = new NeuralNetworkImpl(objNS.getNumberLayer(), objNS.getLayerSize(), objNS.getLearningRate(), objNS.getMomentum());

            } else {
                JOptionPane.showMessageDialog(rootPane, "Wrong inputs for layer sizes");
            }
        }


    }//GEN-LAST:event_btnCreateActionPerformed

