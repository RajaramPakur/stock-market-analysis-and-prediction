    private void btnTrainFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTrainFileActionPerformed

        final JFileChooser fc = new JFileChooser();
        int returnVal = fc.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = fc.getSelectedFile();
            try {
                // What to do with the file, e.g. display it in a TextArea
                textarea.read(new FileReader(file.getAbsolutePath()), null);
//            System.out.println(file.getAbsolutePath());
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(rootPane, "problem accessing file" + file.getAbsolutePath());
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "File access cancelled by user.");
        }

    }//GEN-LAST:event_btnTrainFileActionPerformed

